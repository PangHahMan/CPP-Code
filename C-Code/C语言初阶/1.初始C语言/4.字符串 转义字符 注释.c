void test1() {
    //下面代码，打印结果是什么？为什么？(突出'\0'的重要性)
    char arr1[] = "bit";
    char arr2[] = {'b', 'i', 't'};
    char arr3[] = {'b', 'i', 't', '\0'};
    printf("%s\n", arr1);//bit
    printf("%s\n", arr2);//bit烫烫烫烫
    printf("%s\n", arr3);//bit

    //解释：arr2中没有\0 所以打印arr2的时候 一直在找\0  但是没找到\0  就一直打印烫 直到找到\0为止
}


